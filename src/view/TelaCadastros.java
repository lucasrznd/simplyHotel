    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed

        // Cria a ação do botão excluir vinculado com o banco de dados //
        try {
            // Cria a conexão com o BD //
            Connection con = getConnection();
            Statement st = null;
            String sql = "DELETE FROM `hospedes` where idHospede=?";
            PreparedStatement pst = pst = con.prepareStatement(sql);
            pst.setString(1, idHospede.getText());

            JFrame deletar = new JFrame("Sair");
            if (JOptionPane.showConfirmDialog(deletar, "Realmente deseja deletar esse registro?", "Simply Hotel", JOptionPane.WARNING_MESSAGE,
                    JOptionPane.YES_OPTION) == JOptionPane.YES_NO_OPTION) {
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Registro deletado com sucesso.");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_buttonExcluirActionPerformed

