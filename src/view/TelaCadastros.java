    private void buttonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonConsultarActionPerformed

        // Conexão com BD //
        try {
            // Abrir conexão //
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/cadastrohotel", "root", "1234");

            Statement st = con.createStatement();
            // mySQL query //
            String sql = "SELECT * FROM `hospedes`";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                // Os dados não serão enviados até acabar //
                String idHospede = String.valueOf(rs.getInt("idHospede"));
                String nome = rs.getString("nome");
                String sobrenome = rs.getString("sobrenome");
                String endereco = rs.getString("endereco");
                String cep = rs.getString("cep");
                String celular = rs.getString("celular");
                String email = rs.getString("email");
                String nacionalidade = rs.getString("nacionalidade");
                String dataNas = rs.getString("dataNas");
                String genero = rs.getString("genero");
                String dataCheckin = rs.getString("dataCheckin");
                String dataCheckout = rs.getString("dataCheckout");
                String tipoQuarto = rs.getString("tipoQuarto");
                String numQuarto = rs.getString("numQuarto");
                String valorDiaria = rs.getString("valorDiaria");
                String valorTotal = rs.getString("valorTotal");

                // Array de String para dados na tabela //
                String tbData[] = {idHospede, nome, sobrenome, endereco, cep, celular, email, nacionalidade, dataNas, genero, dataCheckin, dataCheckout, tipoQuarto,
                    numQuarto, valorDiaria, valorTotal};
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

                // Adicionar String array pra jTable //
                model.addRow(tbData);

            }

            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Os dados não podem ser enviados.", "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(TelaCadastros.class.getName()).log(Level.SEVERE, "Erro", e);
        }

    }//GEN-LAST:event_buttonConsultarActionPerformed

